package com.eat.Model;

public class Theme {
	
	private int ThemeId;
	private String ThemeName;
	private String ThemeDescribe;
	private String SpecificName;
	private String ThemePicture;
	private int Detaild;
	private int classcify;
	
	
	public int getThemeId() {
		return ThemeId;
	}
	public void setThemeId(int themeId) {
		ThemeId = themeId;
	}
	public String getThemeName() {
		return ThemeName;
	}
	public void setThemeName(String themeName) {
		ThemeName = themeName;
	}
	public String getThemeDescribe() {
		return ThemeDescribe;
	}
	public void setThemeDescribe(String themeDescribe) {
		ThemeDescribe = themeDescribe;
	}
	public String getSpecificName() {
		return SpecificName;
	}
	public void setSpecificName(String specificName) {
		SpecificName = specificName;
	}
	public String getThemePicture() {
		return ThemePicture;
	}
	public void setThemePicture(String themePicture) {
		ThemePicture = themePicture;
	}
	public int getDetaild() {
		return Detaild;
	}
	public void setDetaild(int detaild) {
		Detaild = detaild;
	}
	public int getClasscify() {
		return classcify;
	}
	public void setClasscify(int classcify) {
		this.classcify = classcify;
	}
	
	

}
